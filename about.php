              <b>Motto:</b>
              <br><br>

              The Motto of NSS "Not Me But You", reflects the essence of democratic living and upholds the need for self-less service. NSS helps the students development & appreciation to other person's point of view and also show consideration towards other living beings. The philosophy of the NSS is a good doctrine in this motto, which underlines on the belief that the welfare of an individual is ultimately dependent on the welfare of the society as a whole and therefore, the NSS volunteers shall strive for the well-being of the society.<br><br>
              <b>NSS Logo:</b><br>
              <br>

              The logo for the NSS has been based on the giant Rath Wheel of the world famous Konark Sun Temple (The Black Pagoda) situated in Orissa, India. The Red & Blue colors contained in the logo motivate the NSS Volunteers to be active & energetic for the nation-building social activities. The wheel portrays the cycle of creation, preservation and release and signifies the movement in life across time and space, The wheel thus stands for continuity as well as change and implies the continuous striving of NSS for social change.<br><br>
              <b>NSS Badge:</b><br><br>

              The NSS logo is embossed on the badge of NSS. The eight bars in the wheel of NSS logo represent the 24 hours of a day. The red colour indicates that the volunteer is full of young blood that is lively, active, energetic and full of high spirit. The navy blue colour indicates the cosmos of which the NSS is tiny part, ready to contribute its share for the welfare of the mankind.<br><br>
              <b>Financial Arrangements:</b><br><br>

              The scheme is now a central sector scheme to organize Regular Activities (RA) and special Camping Programmes (SCPs). An amount of Rs.250/-per volunteer per annum is released towards Regular Activities (RAs) and Rs.450/- per volunteer per Special Camping Programme (SCP) which is of 7-days duration to be organized in the adopted villages/urban slums. In addition, the Government of India provides 100% financial assistance to run NSS Regional Directorates, State NSS Cells and Empanelled Training Institute (ETI).<br><br>
